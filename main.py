# BASIC OPERATIONS AND INPUT 
# Arithmetic Operators
# + (Addition),-(subtraction), *(multiplication), /(division), %(modulus), **(exponentiation) // ()

num_one = input("Enter first number: ")
num_two = input("Enter second number: ")

result = int(num_one) + int(num_two)
result_two = int(num_one) - int(num_two)
result_three = int(num_one) * int(num_two)
result_four = int(num_one) / int(num_two)
result_five = int(num_one) % int(num_two)
result_six = int(num_one) ** int(num_two)
result_seven = int(num_one) // int(num_two)
print(result, result_two, result_three, result_four, result_five, result_six, result_seven)

# Assignment Operators - Assign a value to a variable (+=, -=, *=, /=, %=, **=, //=)
# = Assignment Operator
num_one = 10

# += Addition Assignment Operator
num_one += 5 # num_one = num_one + 5
print(num_one)

# -= Subtraction Assignment Operator
num_one -= 5 # num_one = num_one - 5
print(num_one)

# *= Multiplication Assignment Operator
num_one *= 5 # num_one = num_one * 5
print(num_one)

# /= Division Assignment Operator
num_one /= 2 # num_one = num_one / 5
print(num_one)

# %= Modulus Assignment Operator
num_one %= 2 # num_one = num_one % 5
print(num_one)

# **= Exponentiation Assignment Operator
num_one **= 2 # num_one = num_one ** 5
print(num_one)

# //= Floor Division Assignment Operator
num_one = -5
num_one //= 2 # num_one = num_one // 5
print(num_one)



